public class Aula03 {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 20;

        int sum = num1 + num2;
        int sub = num1 - num2;
        float div = num1 / num2;
        int mul = num1 * num2;

        System.out.println("sum =" + sum);
        System.out.println("subtract =" + sub);
        System.out.println("div =" + div);
        System.out.println("mul =" + mul);
    }
}
